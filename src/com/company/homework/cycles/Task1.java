package com.company.homework.cycles;

/*
* 1. При помощи цикла for вывести на экран нечетные числа от 1 до 99.
     При решении используйте операцию инкремента (++).
* */

public class Task1 {

    public void first() {

        for (int i = 0; i < 99; i++) {
            System.out.println(++i);
        }
    }
}
